/*
Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.
*/
var person_Name = "Muhammad Ali Jinnah";
var result_lowerCase = person_Name.toLowerCase();
console.log(result_lowerCase);
var result_upperCase = person_Name.toUpperCase();
console.log(result_upperCase);
var result_titleCase = person_Name.toLocaleUpperCase();
console.log(result_titleCase);
